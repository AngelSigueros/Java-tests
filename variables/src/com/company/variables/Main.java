package com.company.variables;


public class Main {

    public static void main(String[] args) {
        // TIPOS DE DATOS EN JAVA

        // tipodedato identificador = X;


        // 1. numericos enteros
        byte number1 = 127;
        short number2 = 2;
        int numero3 = 3;
        long numero4 = 4L;

        System.out.println(number1);
        System.out.println(number2);
        System.out.println(numero3);
        System.out.println(numero4);

        // 2. numericos decimales
        float numbero5 = 5.0f;
        double numbero6 = 5.0d;

        System.out.println(numbero5);
        System.out.println(numbero6);

        // 3. boolean
        boolean verdadero = true;
        boolean falso = false;

        System.out.println(verdadero);
        System.out.println(falso);

        // 4. texto
        char caracterA = 'a';
        String texto = "Hola buenas qu√© tal";

        System.out.println(caracterA);
        System.out.println(texto);



    }
}
